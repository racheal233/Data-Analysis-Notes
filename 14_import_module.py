# 引入模块：
# 1---import 模块名字，如statistics ，调用函数时需加上前缀，如statistics.median
# 2---from 模块名字 import 函数名称 ---表示只调用import后面的函数
# 3---from 模块名字 import *      ---表示调动模块所有函数，不推荐！！！，原因是容易造成命名冲突
# 内置函数：https://docs.python.org/zh-cn/3/library/functions.html
# 调用第三方库的模块：Pypi.org  ，安装库去到终端输入：Pip install.库名 ，安装后就可以引入模块